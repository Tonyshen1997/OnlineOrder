export const signup = (data) => {
  const signupUrl = '/signup';

  return fetch(signupUrl, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(data),
  }).then((response) => {
    if (response.status < 200 || response.status >= 300) {
      throw Error('Fail to sign up');
    }
  });
};

